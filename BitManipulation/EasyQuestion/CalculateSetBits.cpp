class Solution {
  public:
    int setBits(int N) {
        // Write Your Code here
        int count=0; 
        while(N)
        {
            if(N&1)//check if odd :: for odd No. right most bit is 1
            {
                count++;
            }
            N>>=1;//divide by 2
        }
        return count;
    }
};

